#include "libft.h"

int		ft_atoi(const char *str)
{
	int		i;
	long	resultat;
	int		po;

	i = 0;
	resultat = 0;
	po = 1;
	while (str[i] == ' ' || str[i] == '\n' || str[i] == '\t'
			|| str[i] == '\r' || str[i] == '\v' || str[i] == '\f')
		i++;
	if (str[i] == '-')
		po = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		resultat = (resultat * 10) + (str[i] - 48);
		i++;
	}
	if (po == -1)
		return (-resultat);
	return (resultat);
}
